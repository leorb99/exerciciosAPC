# Pesquisa de precos

arq = input()
loja = {}

with open(arq) as arquivo:
    cabecalho = arquivo.readline().strip('\n')
    cabecalho = cabecalho.split(',')
    n, m = int(cabecalho[0].split()[0]), int(cabecalho[0].split()[1]) 
    cabecalho.pop(0)

    for linha in arquivo:
        info = linha.strip('\n')
        info = info.split(',')
        loja[info[0]] = []

        for i in range(1, (n + 1)):
            loja[info[0]].append(float(info[i]))

for k in loja:
    print(k, cabecalho[loja[k].index(min(loja[k]))])

